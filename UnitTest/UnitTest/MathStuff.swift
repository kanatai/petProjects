import Foundation

class MathStuff {
    func addNumbers(x: Int, y: Int) -> Int {
        return x + y
    }
    
    func multipleNumbers(x: Int, y: Int) -> Int {
        return x * y
    }
    
    func devideNumbers(x: Int, y: Int) -> Int {
        return x / y
    }
}
